# Faça um algoritmo para calcular o valor do desconto de um produto. O valor do produto é informado na entrada de dados e o desconto é sempre 10%.

print("-----------------------")
valorProduto = float(input("Digite o valor do produto: "))
desconto = valorProduto*0.10

print("Valor do desconto: ", desconto)
print("Valor do produto com o desconto: ", valorProduto-desconto)
